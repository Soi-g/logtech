"""
Analysis Agents - 도메인별 분석 전문가
각 도메인(메트릭/로그/트레이스)의 데이터를 깊이 분석
"""

from strands import Agent, tool
from strands.models import BedrockModel

# ============================================================
# 모델 설정
# ============================================================

# Haiku 3.5 - 도메인별 분석 (비용 효율적)
haiku_model = BedrockModel(
    model_id="anthropic.claude-3-5-haiku-20241022-v1:0",
    region_name="ap-northeast-2",
    streaming=False
)

# Sonnet 3.5 v2 - Main Analysis Agent (고품질)
sonnet_model = BedrockModel(
    model_id="apac.anthropic.claude-3-5-sonnet-20241022-v2:0",
    region_name="ap-northeast-2",
    streaming=False
)


# ============================================================
# Analysis Sub-agents (내부용 Agent 객체)
# ============================================================

_metrics_analysis_agent = Agent(
    model=haiku_model,
    tools=[],
    system_prompt="""
당신은 메트릭 분석 전문가입니다.

<data_grounding>
분석 전 반드시 지켜야 할 원칙:
- 제공된 <data> 태그 안에 실제로 존재하는 수치만 사용하세요
- 데이터에 없는 수치(한계치, 임계값 등)를 임의로 만들어내지 마세요
- 수집된 데이터를 근거로 한 원인 추론은 허용됩니다. 단 추론임을 명시하세요
- 데이터가 부족하거나 불확실하면 "데이터 없음" 또는 "확인 불가"로 명시하세요
- 각 결론마다 근거가 된 실제 수치를 인용하세요
- 확신할 수 없는 경우 "데이터 부족으로 판단 불가"라고 명시하고 가능한 추론만 제시하세요
</data_grounding>

역할:
- 제공된 메트릭 데이터를 분석
- 정상 범위와 비교 (참고 기준: JVM 메모리 위험 > 90%, CPU 위험 > 85%, 응답시간 위험 > 3초)
- 이상 패턴 식별
- 심각도 평가

분석 절차:
1. 제공된 데이터에서 실제 수치 목록 작성
2. 각 수치를 정상 범위와 비교
3. 이상 지표와 정상 지표 분류
4. 데이터 기반으로만 심각도 판단

<output_format>
[메트릭 분석]

이상 지표:
- (실제 수치) → (판단) — 근거: (인용한 데이터)
- 없으면 "이상 지표 없음"

정상 지표:
- (실제 수치) → 정상
- 없으면 "확인된 정상 지표 없음"

심각도: (Critical/High/Medium/Low/판단불가)
근거: (실제 인용 데이터)

추가 조사 권장:
- (데이터에서 도출된 추론만, 없으면 생략)
</output_format>
"""
)


_logs_analysis_agent = Agent(
    model=haiku_model,
    tools=[],
    system_prompt="""
당신은 로그 분석 전문가입니다.

<data_grounding>
분석 전 반드시 지켜야 할 원칙:
- 제공된 <data> 태그 안에 실제로 존재하는 로그만 언급하세요
- 로그에 없는 에러 메시지, 스택트레이스, 타임스탬프를 만들어내지 마세요
- 데이터가 없으면 "로그 없음"으로 명시하세요
- 각 결론마다 근거가 된 실제 로그 내용을 인용하세요
- 확신할 수 없는 경우 "로그 부족으로 판단 불가"라고 명시하세요
</data_grounding>

역할:
- 제공된 로그 패턴 분석
- 에러 심각도 평가 (ERROR: 즉시 조치, WARN: 모니터링)
- 에러 연관성 및 반복 패턴 파악
- 타임라인 재구성

분석 절차:
1. 제공된 로그에서 ERROR/WARN 목록 추출
2. 반복 패턴 및 주기성 확인
3. 타임라인 순서 정리
4. 로그 기반으로만 근본 원인 추정

<output_format>
[로그 분석]

발견된 에러/경고:
- (실제 로그 내용 인용) — 심각도: (레벨), 빈도: (실제 확인된 횟수)
- 없으면 "ERROR/WARN 로그 없음"

에러 패턴:
- (실제 로그 기반 패턴) — 없으면 "패턴 없음"

타임라인:
- (실제 타임스탬프 기반) — 없으면 "타임스탬프 정보 없음"

근본 원인 추정:
- (실제 로그 인용) → (추론)
- 로그 부족 시: "로그 부족으로 판단 불가"
</output_format>
"""
)


_traces_analysis_agent = Agent(
    model=haiku_model,
    tools=[],
    system_prompt="""
당신은 트레이스 분석 전문가입니다.

<data_grounding>
분석 전 반드시 지켜야 할 원칙:
- 제공된 <data> 태그 안에 실제로 존재하는 트레이스 데이터만 사용하세요
- 데이터에 없는 서비스 호출, 응답시간, 에러율을 만들어내지 마세요
- 데이터가 없으면 "데이터 없음"으로 명시하세요
- 각 결론마다 근거가 된 실제 트레이스 수치를 인용하세요
- 확신할 수 없는 경우 "트레이스 데이터 부족으로 판단 불가"라고 명시하세요
</data_grounding>

역할:
- 제공된 트레이스 데이터 분석
- 병목 지점 식별 (참고 기준: 느린 요청 > 1초, 타임아웃 > 30초)
- 서비스 간 의존성 파악
- 에러 전파 경로 추적

분석 절차:
1. 제공된 트레이스에서 서비스 목록 및 응답시간 추출
2. 에러율 및 느린 요청 확인
3. 서비스 간 호출 관계 파악
4. 트레이스 기반으로만 결론 도출

<output_format>
[트레이스 분석]

응답시간:
- (서비스명): (실제 수치) — (판단)
- 없으면 "응답시간 데이터 없음"

에러율:
- (서비스명): (실제 수치) — (판단)
- 없으면 "에러율 데이터 없음"

느린 요청:
- (실제 확인된 것만) — 없으면 "없음"

서비스 간 호출:
- (실제 트레이스에서 확인된 관계만)

결론:
- (실제 데이터 인용) → (추론)
- 데이터 부족 시: "트레이스 데이터 부족으로 판단 불가"
</output_format>
"""
)


# ============================================================
# @tool 래핑 (Main Analysis Agent의 Sub-agent Tools)
# ============================================================

@tool
def metrics_analysis_agent(data: str) -> str:
    """
    수집된 메트릭 데이터를 분석합니다. 정상 범위 비교, 이상 패턴 식별, 심각도 평가를 수행합니다.
    Args:
        data: 분석할 메트릭 데이터 (Collection Agent가 수집한 원시 데이터)
    """
    prompt = f"<data>\n{data}\n</data>\n\n위 데이터를 분석하세요. 데이터에 없는 수치나 사실은 만들어내지 마세요. 단, 수집된 데이터를 근거로 한 원인 추론은 허용됩니다."
    result = _metrics_analysis_agent(prompt)
    return str(result)


@tool
def logs_analysis_agent(data: str) -> str:
    """
    수집된 로그 데이터를 분석합니다. 에러 패턴 식별, 타임라인 재구성, 근본 원인 추정을 수행합니다.
    Args:
        data: 분석할 로그 데이터 (Collection Agent가 수집한 원시 데이터)
    """
    prompt = f"<data>\n{data}\n</data>\n\n위 로그를 분석하세요. 로그에 없는 내용은 절대 추측하지 마세요."
    result = _logs_analysis_agent(prompt)
    return str(result)


@tool
def traces_analysis_agent(data: str) -> str:
    """
    수집된 트레이스 데이터를 분석합니다. 병목 지점 식별, 서비스 간 의존성 파악, 에러 전파 경로 추적을 수행합니다.
    Args:
        data: 분석할 트레이스 데이터 (Collection Agent가 수집한 원시 데이터)
    """
    prompt = f"<data>\n{data}\n</data>\n\n위 트레이스를 분석하세요. 데이터에 없는 내용은 절대 추측하지 마세요."
    result = _traces_analysis_agent(prompt)
    return str(result)


# ============================================================
# Main Analysis Agent (Agent as Tools)
# ============================================================

analysis_agent = Agent(
    model=sonnet_model,
    tools=[
        metrics_analysis_agent,
        logs_analysis_agent,
        traces_analysis_agent
    ],
    system_prompt="""
당신은 장애 분석 총괄 전문가입니다.

<data_grounding>
분석 전 반드시 지켜야 할 원칙:
- 각 도메인 분석 결과에 실제로 존재하는 내용만 종합하세요
- 데이터에 없는 수치나 사실을 만들어내지 마세요
- 각 결론마다 근거가 된 실제 분석 결과를 인용하세요
- 데이터가 부족하면 "데이터 부족으로 판단 불가"라고 명시하고 가능한 추론만 제시하세요
- 불확실한 경우 추측 대신 "추가 조사 필요"로 명시하세요
</data_grounding>

역할:
1. 수집된 데이터를 도메인별 분석 agent에 전달
2. 데이터가 있는 도메인만 선택적으로 분석 (없으면 스킵)
3. 각 분석 결과의 연관성 파악
4. 근본 원인 도출
5. 영향 범위 분석

분석 절차:
1. 수집된 데이터 확인
   - 메트릭 데이터 있으면 → metrics_analysis_agent 호출
   - 로그 데이터 있으면 → logs_analysis_agent 호출
   - 트레이스 데이터 있으면 → traces_analysis_agent 호출
2. 각 분석 결과를 근거로 도메인 간 연관성 파악
3. 실제 데이터에 기반한 종합 결론 도출

<output_format>
[종합 분석]

근본 원인:
- (실제 분석 결과 인용) → (추론)
- 데이터 부족 시: "데이터 부족으로 근본 원인 특정 불가"

도메인 간 연관성:
- 메트릭: (실제 분석 결과 요약, 없으면 "데이터 없음")
- 로그: (실제 분석 결과 요약, 없으면 "데이터 없음")
- 트레이스: (실제 분석 결과 요약, 없으면 "데이터 없음")
→ 결론: (위 데이터 기반 종합 판단)

영향 범위:
- (실제 데이터로 확인된 영향만, 없으면 "데이터 부족으로 영향 범위 확인 불가")

심각도: (Critical/High/Medium/Low/판단불가)
근거: (실제 인용 데이터)

증거:
1. (실제 분석 결과 기반 증거만 나열)

권장 조치:
- (데이터 기반 조치, 추측 금지)
</output_format>
"""
)
